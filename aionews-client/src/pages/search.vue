<template>
  <div class="search-flow-page">
    <article-list-header icon="icon-search">
      <i18n :zh="`和 “${keyword}” 有关的所有文章`" :en="`Keyword &quot;${keyword}&quot;\'s result`"
        :vi="`Từ khoá tìm kiếm ${keyword}`" />
    </article-list-header>
    <article-list :fetching="articleListStore.list.fetching" :articles="articleListStore.list.data"
      :pagination="articleListStore.list.pagination" @loadmore="loadmoreArticles" />
  </div>
</template>

<script lang="ts">
  import { defineComponent, watch, onBeforeMount } from 'vue'
  import { useUniversalFetch, onClient } from '/@/universal'
  import { useEnhancer } from '/@/app/enhancer'
  import { useArticleListStore } from '/@/stores/article'
  import { scrollToNextScreen } from '/@/utils/scroller'
  import ArticleListHeader from '/@/components/flow/desktop/header.vue'
  import ArticleList from '/@/components/flow/desktop/list.vue'

  export default defineComponent({
    name: 'SearchPage',
    components: {
      ArticleListHeader,
      ArticleList
    },
    props: {
      keyword: {
        type: String,
        required: true
      }
    },
    setup(props) {
      const { meta } = useEnhancer()
      const articleListStore = useArticleListStore()

      meta(() => ({ pageTitle: `${props.keyword} | Search` }))

      const fetchArticles = (params: any) => {
        return articleListStore.fetchList(params)
      }

      const loadmoreArticles = async () => {
        await fetchArticles({
          keyword: props.keyword,
          page: articleListStore.list.pagination.current_page + 1
        })
        onClient(scrollToNextScreen)
      }

      onBeforeMount(() => {
        watch(
          () => props.keyword,
          (keyword) => fetchArticles({ keyword }),
          { flush: 'post' }
        )
      })

      useUniversalFetch(() => fetchArticles({ keyword: props.keyword }))

      return {
        articleListStore,
        loadmoreArticles
      }
    }
  })
</script>
