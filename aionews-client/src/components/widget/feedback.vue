  import { LanguageKey } from '/@/language/key'
  import { GAEventCategories } from '/@/constants/gtag'
  import { useEnhancer } from '/@/app/enhancer'
  import { useUniversalStore } from '/@/stores/universal'
  import { useMetaStore } from '/@/stores/meta'
  import { META } from '/@/config/app.config'

  enum Event {
    Close = 'close'
  }

  export default defineComponent({
    name: 'Feedback',
    emits: [Event.Close],
    setup(_, context) {
      const emotions = [
        { emoji: 'üò†', value: 1, en: 'Terrible', zh: 'Â∑ÆÂä≤', vi: 'Kinh kh·ªßng' },
        { emoji: 'üôÅ', value: 2, en: 'Bad', zh: '‰∏çÂñúÊ¨¢', vi: 'T·ªá' },
        { emoji: 'üòê', value: 3, en: 'Normal', zh: '‰∏ÄËà¨', vi: 'B√¨nh th∆∞·ªùng' },
        { emoji: 'üôÇ', value: 4, en: 'Good', zh: 'Â•Ω', vi: 'T·ªët' },
        { emoji: 'üòÑ', value: 5, en: 'Great', zh: 'ÈùûÂ∏∏Â•Ω', vi: 'Tuy·ªát v·ªùi' }
      ]

      const metaStore = useMetaStore()
      const universalStore = useUniversalStore()
      const { gtag, isZhLang, isEnLang } = useEnhancer()
      const historyFeedbacks = computed(() => universalStore.feedbacks)
      const state = reactive({
        emotion: null as unknown as number,
        content: '',
        submitting: false,
        submitted: false
      })
      const isSubmitable = computed(() => {
        return Number.isInteger(state.emotion) && Boolean(state.content)
      })

      const handleClose = () => context.emit(Event.Close)
      const handleSubmit = () => {
        if (isSubmitable.value) {
          gtag?.event('submit_feedback', {
            event_category: GAEventCategories.Widget
          })

          state.submitting = true
          metaStore
            .postFeedback(toRaw(state))
            .then((result) => {
              universalStore.addFeedback(result.result)
              state.submitted = true
            })
            .catch((error: any) => {
              alert(error)
            })
            .finally(() => {
              state.submitting = false
            })
        }
      }

      return {
        LanguageKey,
        META,
        isZhLang,
        isEnLang,
        isSubmitable,
        emotions,
        state,
        historyFeedbacks,
        handleClose,
        handleSubmit
      }
    }
  })
</script>

